from graph import BPMNGraph

def main():
    # Вводим примерный текст (или можно заменить на запрос от пользователя)
    text = "Построить диаграмму, которая начинается с события Рождество. Должна быть опция пойти за покупками, с выбором между тремя результатами: ноутбук, айфон, машина. Для того чтобы войти в магазин, нужно иметь деньги."

    # Инициализируем граф
    graph = BPMNGraph()

    # Обрабатываем текст через граф
    final_json = graph.process_text(text)

    # Печатаем финальный JSON
    print("Final BPMN JSON Output:")
    print(final_json)

if __name__ == "__main__":
    main()